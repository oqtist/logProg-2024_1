function bemVindo() {
    let nomeAluno = prompt('Qual é o seu nome?')
    alert(`Boas vindas, ${nomeAluno}.`)
}

function nomeIdade() {
    let nomeAluno = prompt('Qual é o seu nome?')
    let idadeAluno = parseInt(prompt('E a sua idade?'))
    alert(`Seu nome é ${nomeAluno} e você tem ${idadeAluno} anos.`)
}